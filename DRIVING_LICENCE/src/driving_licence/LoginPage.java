    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id=jTextField1.getText(), pw=String.valueOf(jPasswordField1.getPassword());int f=0;
        try
        {
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/DrivingLicence","root","");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("SELECT * FROM Search;");
            
            while(rs.next())
            {
                String ID=rs.getString(1), PW=rs.getString(2);
                if(ID.equals(id) && PW.equals(pw))
                {
                    JOptionPane.showMessageDialog(null, "Logged in.");
                    dispose();
                    new MainMenu().setVisible(true);
                    f=1;
                    break;
                }
            }
            if(f==0)
                JOptionPane.showMessageDialog(null, "Invalid Login ID or Password or both!\nIf you are not registered, then please \'Sign Up\'.");
            con.close();st.close();rs.close();
        }
        catch(Exception e)
        {}
    }//GEN-LAST:event_jButton1ActionPerformed

